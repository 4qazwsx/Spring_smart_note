package oracle.java.note.model;

public class Subject {
	String sub_id;
	String mem_id;
	String sub_name;
	String sub_day;
	String sub_start;
	String sub_time;
	String sub_description;
	String sub_color;
	
	
	public String getSub_id() {
		return sub_id;
	}
	public void setSub_id(String sub_id) {
		this.sub_id = sub_id;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getSub_name() {
		return sub_name;
	}
	public void setSub_name(String sub_name) {
		this.sub_name = sub_name;
	}
	public String getSub_day() {
		return sub_day;
	}
	public void setSub_day(String sub_day) {
		this.sub_day = sub_day;
	}
	public String getSub_start() {
		return sub_start;
	}
	public void setSub_start(String sub_start) {
		this.sub_start = sub_start;
	}
	public String getSub_time() {
		return sub_time;
	}
	public void setSub_time(String sub_time) {
		this.sub_time = sub_time;
	}
	public String getSub_description() {
		return sub_description;
	}
	public void setSub_description(String sub_description) {
		this.sub_description = sub_description;
	}
	public String getSub_color() {
		return sub_color;
	}
	public void setSub_color(String sub_color) {
		this.sub_color = sub_color;
	}
}
